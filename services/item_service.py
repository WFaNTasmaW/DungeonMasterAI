    def randomize(
        self,
        category,
        subcategory,
    ):

        if category == "Любая":
            category = random.choice(self.categories)

        if subcategory in ("Любой", "Любая"):

            variants = self.subcategories.get(category, ["Любой"])

            variants = [
                x for x in variants
                if x not in ("Любой", "Любая")
            ]

            if variants:
                subcategory = random.choice(variants)

        return category, subcategory

